function OpenLetter_OnCreate(items, result, player)
	local random = ZombRand(#SFRelax["Letter"]);
	local randItem = SFRelax["Letter"][random];

	local newItem = player:getInventory():AddItem(randItem);
	if randItem == "filcher.SFLetterToYou" then
		local randomName = ZombRand(#SFRelax["Forenames"]);
		local forename = SFRelax["Forenames"][randomName];
		local randomFamily = ZombRand(#SFRelax["Surnames"]);
		local surname = SFRelax["Surnames"][randomFamily];
		if forename == surname then
			local newFamily = randomFamily - ZombRand(5);
			surname = SFRelax["Surnames"][newFamily];
		end
		newItem:setName(getText("ContextMenu_LetterTo") .. forename .. " " .. surname);
	end
end

function getRandomItemName()
	local random = ZombRand(1, #SFRelax["ItemNames"]);
	local itemName = ScriptManager.instance:getItem(SFRelax["ItemNames"][random]):getDisplayName()
	if ZombRand(1, 100) < 10 then
		random = ZombRand(1, #SFRelax["ItemNamesRare"]);
		itemName = ScriptManager.instance:getItem(SFRelax["ItemNamesRare"][random]):getDisplayName()
	end
	if ZombRand(1, 100) < 20 then
		local randomAppend = ZombRand(1, #SFRelax["Append"]);
		local append = getText(SFRelax["Append"][randomAppend]);
		if ZombRand(1, 100) < 10 then
			randomAppend = ZombRand(1, #SFRelax["AppendRare"]);
			append = getText(SFRelax["AppendRare"][randomAppend], SFRelax["Forenames"][ZombRand(1, #SFRelax["Forenames"])], SFRelax["Surnames"][ZombRand(1, #SFRelax["Surnames"])]);
		end
		itemName = itemName .. append;
	end
	
	return itemName
end

function shoppinglistFiller()
	local randomHeader = ZombRand(1, #SFRelax["ListHeaders"]);
	local header = SFRelax["ListHeaders"][randomHeader];
	local headerFilled = getText(getText(header), SFRelax["Forenames"][ZombRand(1, #SFRelax["Forenames"])], getRandomItemName(), getRandomItemName(), getRandomItemName());
	local shoppingList = headerFilled;
	if ZombRand(2) > 0 then
		local randomExtender = ZombRand(1, #SFRelax["ListExtenders"]);
		local extender = SFRelax["ListExtenders"][randomExtender];
		local extenderFilled = getText(getText(extender), getRandomItemName(), getRandomItemName(), getRandomItemName(), getRandomItemName());
		shoppingList = headerFilled .. extenderFilled;
	end
	if ZombRand(100) < 20 then
		local randomFoot = ZombRand(1, #SFRelax["FootNoteLight"]);
		local footNote = getText(SFRelax["FootNoteLight"][randomFoot]);
		if ZombRand(100) < 20 then
			randomFoot = ZombRand(1, #SFRelax["FootNoteSerious"]);
			footNote = getText(SFRelax["FootNoteSerious"][randomFoot]);
		end
		shoppingList = shoppingList .. footNote;
	end
	return shoppingList
end

function commonNoteFiller()
	local randomNote = ZombRand(1, #SFRelax["Notes"]);
	local note = SFRelax["Notes"][randomNote];
	local randomName = ZombRand(1, #SFRelax["Forenames"]);
	local forename = SFRelax["Forenames"][randomName];
	local randomFamily = ZombRand(1, #SFRelax["Surnames"]);
	local surname = SFRelax["Surnames"][randomFamily];
	local randomLoc = ZombRand(1, #SFRelax["Locations"]);
	local location = SFRelax["Locations"][randomLoc];
	local randomPlace = ZombRand(1, #SFRelax["Places"]);
	local place = SFRelax["Places"][randomPlace];
	local randomRel = ZombRand(1, #SFRelax["Relatives"]);
	local relative = SFRelax["Relatives"][randomRel];
	
	local commonNote = getText(getText(note), forename, surname, getText(relative), getText(place), location);
	return commonNote
end

function CreateNote(item)
	local text = nil;
	if ZombRand(1, 100) > 30 then
			text = shoppinglistFiller();
	else
			text = commonNoteFiller();
	end
	item:addPage(1, text);	
	item:setLockedBy("SFRelax");
end